    public void PreencheServico(Integer codigo) {
        try {
            Cliente c = new Cliente();
            ClienteDAO cdao = new ClienteDAO();

            c.setIdCliente(codigo);
            c = cdao.preencher(c);

            txtCod.setText(c.getIdCliente().toString());
            txtCliente.setText(c.getNomeCliente());

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro inesperado: " + ex.getMessage());
        }
    }

