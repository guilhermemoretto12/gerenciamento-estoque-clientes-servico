    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // CONFIRMAR
        if(txtData.getText().equals("  /  /    ") || txtDescricao.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }
        
        try {
            Servico s = new Servico();
            
            Servico.setIdServico(Conexao.getGenerator("GSERVICO"));
            
            s.setRefServico(s.getIdServico());
            s.setDataServico(txtData.getText());
            s.setDescricao(txtDescricao.getText());

            MedicoDAO md = new MedicoDAO();
            Medico m = md.getMedicoNo(jComboBox1.getSelectedItem().toString());
            s.setIdMedico(m);
            
            Cliente c = new Cliente();
            c.setIdCliente(parseInt(txtCod.getText()));
            s.setIdCliente(c);
            
            ServicoDAO sdao = new ServicoDAO();
            sdao.inserir(s);
            
            dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar serviço: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

