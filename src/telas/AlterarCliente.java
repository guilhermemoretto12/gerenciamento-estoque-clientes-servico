    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // ALTERAR
        if (txtNomeCliente.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o nome!");
            return;
        }

        try{
            Cliente c = new Cliente();  
            ClienteDAO cdao = new ClienteDAO();
            
            c.setNomeCliente(txtNomeCliente.getText());
            if(txtCPFCliente.getText().equals("   .   .   -  ")){
                c.setCpf(null);
            }else{
                c.setCpf(txtCPFCliente.getText());
            }
            if(txtDataNascimentoCliente.getText().equals("  /  /    ")){
                c.setDataNascimento(null);
            }else{
                c.setDataNascimento(txtDataNascimentoCliente.getText());
            }
            if(txtTelefone1Cliente.getText().equals("(  )     -    ")){
                c.setTelefone1(null);
            }else{
                c.setTelefone1(txtTelefone1Cliente.getText());
            }
            if(txtTelefone2Cliente.getText().equals("(  )     -    ")){
                c.setTelefone2(null);
            }else{
                c.setTelefone2(txtTelefone2Cliente.getText());
            }
            c.setEstado(txtEstadoCliente.getText());
            c.setCidade(txtCidadeCliente.getText());
            c.setBairro(txtBairroCliente.getText());
            c.setLogradouro(txtLogradouroCliente.getText());
            c.setNumero(txtNumeroCliente.getText());
            c.setPais1(txtPais1Cliente.getText());
            c.setPais2(txtPais2Cliente.getText());
            if(txtCEPCliente.getText().equals("  .   -   ")){
                c.setCep(null);
            }else{
                c.setCep(txtCEPCliente.getText());
            }
            
            cdao.alterar(c);
            dispose();
            ConsultaCliente cl = new ConsultaCliente();
            cl.setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar cliente: " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton15ActionPerformed

