    private void txtCEPClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCEPClienteActionPerformed
        // ENTER NO CEP
        ViaCEP viaCEP = new ViaCEP();

        try {
            String cep = txtCEPCliente.getText();
            cep = cep.replace(".", "");
            viaCEP.buscar(cep);
            txtCidadeCliente.setText(viaCEP.getLocalidade());
            txtBairroCliente.setText(viaCEP.getBairro());
            txtEstadoCliente.setText(viaCEP.getUf());
            txtLogradouroCliente.setText(viaCEP.getLogradouro());

            txtNumeroCliente.requestFocus();
        } catch (ViaCEPException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_txtCEPClienteActionPerformed

